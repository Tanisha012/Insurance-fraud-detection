    "    def calculate_woe(data, feature, target):\n",
    "        grouped = data.groupby(feature)[target].agg(['sum', 'count'])\n",
    "        grouped['non_event'] = grouped['count'] - grouped['sum']\n",
    "        grouped['event_rate'] = grouped['sum'] / grouped['count']\n",
    "        grouped['non_event_rate'] = grouped['non_event'] / grouped['count']\n",
